#include <stdio.h>
#include <stdlib.h>


/**
 * add - addition
 * @a: number
 * @b: nuber
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtraction
 * @a: number
 * @b: nuber
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: number
 * @b: nuber
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * divi - division
 * @a: number
 * @b: nuber
 * Return: result
 */

int divi(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: number
 * @b: nuber
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
